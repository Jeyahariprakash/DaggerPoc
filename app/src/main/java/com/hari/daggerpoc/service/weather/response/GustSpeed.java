public class GustSpeed {

    private String bft;
    private String kn;
    private String kmh;
    private String mph;
    private String ms;

    /**
     *
     * @return
     * The bft
     */
    public String getBft() {
        return bft;
    }

    /**
     *
     * @param bft
     * The bft
     */
    public void setBft(String bft) {
        this.bft = bft;
    }

    /**
     *
     * @return
     * The kn
     */
    public String getKn() {
        return kn;
    }

    /**
     *
     * @param kn
     * The kn
     */
    public void setKn(String kn) {
        this.kn = kn;
    }

    /**
     *
     * @return
     * The kmh
     */
    public String getKmh() {
        return kmh;
    }

    /**
     *
     * @param kmh
     * The kmh
     */
    public void setKmh(String kmh) {
        this.kmh = kmh;
    }

    /**
     *
     * @return
     * The mph
     */
    public String getMph() {
        return mph;
    }

    /**
     *
     * @param mph
     * The mph
     */
    public void setMph(String mph) {
        this.mph = mph;
    }

    /**
     *
     * @return
     * The ms
     */
    public String getMs() {
        return ms;
    }

    /**
     *
     * @param ms
     * The ms
     */
    public void setMs(String ms) {
        this.ms = ms;
    }

}
